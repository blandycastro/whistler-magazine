            <h2>Summer in Whistler sees Fine Dining emjoyed outside the box</h2>
            <h3 class="byline">By Cindy Filipenko</h3>

            <figure>
                <img src="images/stories/fine-dining-02.jpg" alt="" />
                <figcaption>Araxi Longtable dinner at Lost Lake in Whistler. Photo courtesy Araxi, <a target="_blank" href="http://www.araxi.com">araxi.com</a></figcaption>
            </figure>

            <p class="story-lead">Foodies rejoice — Summer in Whistler means fine dining moves into parks, fields and skies.</p>
            <p>Skies? Indeed.</p>
            <p>The Bearfoot Bistro, known for exceptional events like Masquerave and the World Oyster Invitational, is introducing another one this summer. Saturday, June 28 marks the debut of Skyhigh: A Dinner with Altitude on the Peak 2 Peak Gondola.</p>
            <p>“We’re very pleased to be partnered with the Whistler Blackcomb Foundation to do this unique fundraiser,” says Marc Des Rosiers, marketing guru for the Bearfoot. “It’s an exciting, different — a new kind of event.”</p>

            <figure>
                <img src="images/stories/fine-dining-01.jpg" alt="" />
                <figcaption>The Bearfoot Bistro’s Skyhigh dinner, aboard the Peak 2 Peak Gondola. Photo courtesy Bearfoot Bistro, <a target="_blank" href="http://www.bearfootbistro.com">bearfootbistro.com</a></figcaption>
            </figure>

            <p>Tickets for Skyhigh, a cool $7,500 per gondola cabin, provide guests with a six-course meal for up to 10 people that will be served during multiple crossings between Whistler and
Blackcomb on the record-breaking Peak 2 Peak. The evening also includes a mountaintop cocktail reception at Christine’s restaurant in Blackcomb’s Rendezvous Lodge before dinner, and an
extraordinary “after party” at the Bearfoot.</p>
            <p>Guests will take in breathtaking panoramas, 424 metres above the valley floor, as they enjoy Chef Melissa Craig’s sixcourse menu, featuring food that’s as beautiful as it is delicious.
And they’ll never imagine the considerable logistics behind their evening.</p>
            <p>“It’s all about timing. We have to time the courses with the crossings and our staff has to be able to clear and serve during the 90 seconds the gondolas are at the loading platforms,”
explains Des Rosiers.</p>
            <p>Skyhigh will definitely be an exciting evening for guests and staff alike. Expect all that is the Bearfoot during this once-in-a-lifetime dining experience, which will come with an unparalleled view. With proceeds benefiting the Whistler Blackcomb Foundation, the unique dinner will help to fund important community initiatives in the Sea to Sky Corridor. Visit <a href="http://www.bearfootbistro.com">bearfootbistro.com</a> for more details.</p>

            <h3>Araxi Longtable Series | Taking it outside</h3>
            <figure>
                <img src="images/stories/fine-dining-03.jpg" alt="" />
                <figcaption>Araxi sous chef Jim Pickwell samples some herbs at North Arm Farm. Photo by David Buzzard, <a target="_blank" href="http://www.media-centre.ca">media-centre.ca</a></figcaption>
            </figure>
            <p>For 32 years, Araxi has been wowing visitors and locals with its decidedly West Coast menu, which emphasizes local and organic produce, meats and seafood. For the past seven years, Chef James Walt has been taking the Araxi experience outside, first as part of Outstanding in the Field — a long-running, outdoor dining series with events across North America — and for the past four years with the restaurant’s own Longtable Series. Debuting at Pemberton’s North Arm Farm in 2011, the series now includes dinners at Lost Lake in Whistler and Vanier Park in Vancouver.</p>
            <blockquote>“It’s amazing to get great food where you least expect it — up against Mount Currie, on a farm.”</blockquote>
            <p>While he’s excited about the newer events, the Longtable dinner closest to Walt’s heart remains Pemberton, largely due to the incredible location.</p>
            <p>“It’s amazing to get great food where you least expect it — up against Mount Currie, on a farm,” says Walt. “It’s great to point to a raspberry 10 feet from the table and say, ‘That’s in the dessert.’”</p>
            <p>The linen-covered table seats 150 guests, and hosts the freshest organic foods available. Brilliantly prepared on two site-specific kitchens and served family style, the food replicates the Araxi experience in the great outdoors.</p>
            <p>“We don’t cut corners. It’s the same food and the same quality you’d get at the restaurant,” says the accomplished chef. “We just don’t plate up individual meals.”</p>
            <p>Dates for this summer’s Longtable dinners are: Vanier Park (Aug. 4), North Arm Farm (Aug. 16) and Lost Lake (Aug. 30). The $175 tickets include wine pairings chosen by Araxi’s Samantha
Rahn, named Sommelier of the Year at the 2013 Vancouver International Wine Festival. Visit araxi.com for more information.</p>

            <h3>Diner en Blanc | The ultimate surprise picnic</h3>
            <figure>
                <img src="images/stories/fine-dining-04.jpg" alt="" />
                <figcaption>Diners enjoy the freshest organic foods against the stunning backdrop of Mount Currie near Pemberton, at the Araxi Longtable Series. Photo by David Buzzard, <a target="_blank" href="http://www.media-centre.ca">media-centre.ca</a></figcaption>
            </figure>
            <p>Fresh to Whistler this summer is Diner en Blanc, an event founded 26 years ago by François Pasquier as a way to reconnect with a Parisian friend. At this stylish picnic, all diners wear white to recognize each other when meeting at a secret location — one that’s not revealed until the day before. It’s the biggest surprise dinner party in the world. The annual gathering is hosted in more than 40 cities, with the Paris event now attracting 10,000.</p>
            <p>Producers Kristen Robinson, Andy Wadsworth and Anna Pellici are bringing Diner en Blanc to the resort. On Aug. 28, a picnic of 500 people outfitted in summer whites will suddenly appear somewhere in Whistler.</p>
            <p>Wadsworth said he saw potential for the event to work in Whistler after participating in Vancouver’s Diner en Blanc, which now has a 6,000-person waitlist.</p>
            <p>Traditionally, guests bring everything — from their table and chairs and all the epicurean delights fit for a memorable evening of dining and dancing al fresco. At Whistler’s
first Diner en Blanc, guests will have the option of having the heavy lifting taken care of by the experts.</p>
            <p>“If all you want to do is show up wearing white, we can get everything else ready. Food. Wine. Tableware. Anything you’ll need for a great evening, can be ordered online
and waiting for you when you arrive,” says Robinson.</p>
            <p>With Bearfoot Bistro on board to provide catering for this very elegant picnic, French flair is assured. For more information about Diner en Blanc, visit <a href="http://whistler.dinerenblanc.info">whistler.dinerenblanc.info</a>.</p>

            <h3>Cornucopia | Fall Festival Takes Over Town</h3>
            <figure>
                <img src="images/stories/fine-dining-05.jpg" alt="" />
                <figcaption>Chefs prepare fresh salads at the Cornucopia Chef’s Luncheon, a feast in a luxurious, private Whistler home. Photo by Mike Crane/Tourism Whistler, <a target="_blank" href="http://www.mikecranephotography.com">mikecranephotography.com</a></figcaption>
            </figure>
            <p>Once the Summer is gone, the annual Cornucopia Festival celebrates the bounty of the season’s labours with a harvest festival like no other. Over 11 days, Nov. 6 to 16, more than 200
events will keep all corners of the resort hopping for this celebration of food and wine.</p>
            <p>“A lot of our events are outside of restaurants, many, like Crush, taking place in the Whistler Conference Centre,” explains producer Sue Eckersley.</p>
            <blockquote>"It’s casual and it’s exclusive. You’re in a person’s kitchen, not a restaurant kitchen."</blockquote>
            <p>Among the most popular events is the Chef’s Luncheon, where up to 50 strangers gather in one of Whistler’s grand mountain homes to feast on the creations of a top chef.</p>
            <p>“It’s casual and it’s exclusive. You’re in a person’s kitchen, not a restaurant kitchen, and you have the unique opportunity of watching a great chef at work, up close,” says Eckersley. “Most of the chefs we feature come from Vancouver and farther afield.”</p>
            <p>Past chefs have included the legendary Rob Feenie and Vancouver Four Seasons innovator Ned Bell. Tickets for the Chef’s Luncheon, which include wine pairings, are $125.</p>
            <p>For more information, go to <a href="http://www.whistlercornucopia.com">whistlercornucopia.com</a>.</p>
