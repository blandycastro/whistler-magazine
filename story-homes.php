            <h2>Love of nature and community inspired one family to build a home in Whistler that embraces both</h2>
            <h3 class="byline">By Christine Keuling</h3>

            <figure>
                <img src="images/stories/homes.jpg" alt="" />
                <figcaption>Photo by Kristen McGaughey, <a target="_blank" href="http://www.kmcgphoto.com">kmcgphoto.com</a></figcaption>
            </figure>

            <p class="story-lead">They were only supposed to stay for a year.</p>
            <p>When Matt and Nancy Saver decided to move to Whistler in 2003, the plan was to give the resort a 12-month trial. Their children, Cooper and Shay, then aged 7 and 10, agreed to leave their Malibu private school behind and give mountain life a shot.</p>
            <p>More than 10 years later, the Savers are still proud to call Whistler home.</p>
            <p>It was with a sense of reverence and gratitude for their mountain life that they approached the building of their estate on Hillcrest Lane in Whistler’s Alta Vista neighbourhood.</p>
            <p>“We wanted to create something with consistency and harmony, a home that is interesting architecturally while still blending in — which is kind of what Whistler is all about,” explains Nancy.</p>
            <p>Inspired by contemporary mountain retreats in Colorado, and by the local work of architect Andreas Kaminski, the Savers hired Kaminski’s firm, AKA Architecture, to begin writing down the bones of the home they envisioned.</p>

            <figure>
                <img src="images/stories/homes-02.jpg" alt="" />
                <figcaption>Native limestone walls, exposed cedar beams and 360-degree views blur the lines between indoors and out in the open-plan kitchen and dining room. A live edge ancient maple dining table completes the illusion of dining al fresco. Photo by Kristen McGaughey, <a target="_blank" href="http://www.kmcgphoto.com">kmcgphoto.com</a></figcaption>
            </figure>

            <p>The vision was to make the best use of the site’s existing terrain while preserving as much natural beauty as possible. The result is a home built right into a saddle-shaped lot with unique forest, mountain and lake views from every room. It all culminates in a dramatic, cantilevered “tree house” living area with 360-degree architectural glass walls.</p>
            <p>With roof lines mirroring the angles of the topography and floor, and ceiling windows bringing the outdoors in, it’s clear the Savers succeeded in their quest to design a home for living in harmony with nature.</p>
            <p>As seamlessly as the Savers brought their home to the land, they also brought the land to their home by choosing natural and local elements to complete the interior.</p>
            <p>“Bringing the natural elements in was important to us,” says Matt. “I think that’s what keeps a contemporary (home) from becoming too sterile.”</p>

            <figure>
                <img src="images/stories/homes-03.jpg" alt="" />
                <figcaption>The curve of the freestanding stone Palazzo tub and smooth profile of the Caesarstone vanity lend a tranquil quality to the spa-like master bath. A 10-foot opaque glass shower enclosure ensures privacy while allowing in natural light. Photo by Kristen McGaughey, <a target="_blank" href="http://www.kmcgphoto.com">kmcgphoto.com</a></figcaption>
            </figure>
            <figure>
                <img src="images/stories/homes-04.jpg" alt="" />
                <figcaption>Warm wood-framed windows, a sleek fireplace, and forest views highlight the relationship between natural elements and modern lines played out in this bedroom and throughout the house. Photo by Kristen McGaughey, <a target="_blank" href="http://www.kmcgphoto.com">kmcgphoto.com</a></figcaption>
            </figure>
            <figure>
                <img src="images/stories/homes-05.jpg" alt="" />
                <figcaption>Understated appliances and warm millwork make this courtyard-facing kitchen one of the coziest rooms in the house. A crevasse prep sink on an expansive centre island makes preparing dinner a pleasantly social task. Photo by Kristen McGaughey, <a target="_blank" href="http://www.kmcgphoto.com">kmcgphoto.com</a></figcaption>
            </figure>

            <p>Nancy designed the interior spaces with native limestone, exposed cedar beams, Brazilian Cherry hardwood floors and maple millwork, all in a consistent, warm colour palette — the perfect foil for the 15- to 18-foot white walls framing the living spaces. The clean, white walls were intended to showcase the couple’s carefully curated and much-appreciated collection of contemporary art. Large-scale paintings by artists Joe Goode, Peter Alexander and Rossan Crowe were brought in — some by crane — to adorn the walls of the living spaces. In the evening, passersby stop to see the pieces lit up against the backdrop of the Savers’ home and the surrounding forest.</p>
            <p>Inside the home, the flow of the living space is both expansive and contractive by turns. Organized around a central spine with the living and dining rooms facing outwards to wide-open lake and mountain views, the bedrooms and kitchen are organized around a quiet courtyard, lawn, pond and waterfall.</p>

            <blockquote>“Bringing the natural elements in was important to us. I think that’s what keeps a contemporary (home) from becoming too sterile.”</blockquote>

            <figure>
                <img class="align-right" src="images/stories/homes-06.jpg" alt="" />
                <figcaption>Custom-stained maple millwork throughout the home provides ample square footage to stash necessities or display treasures like a collection of art books or family photographs. Millwork by Dynamic Manufacturing. Photo by Anastasia Chomlack, <a target="_blank" href="http://www.anastasiaphotography.ca">anastasiaphotography.ca</a></figcaption>
            </figure>

            <p>During the ski season, while Matt checks lift lines and watches a parade of boot packers on Spanky’s Ladder from the telescope in the suspended living room, Nancy can retreat to her reading room overlooking the courtyard with nobody in view. Except, perhaps, the Savers’ dog, Ever, sunning herself on a favourite rock.</p>
            <p>The courtyard is the creation of local landscapers SoundGarden Landscape and Design, who the Savers hired to carefully preserve existing trees, plants and rock features, and bring in native plants to complete the effect of a home nestled into the topography.</p>
            <figure>
                <img src="images/stories/homes-07.jpg" alt="" />
                <figcaption>Relaxed Montauk sofas in modern white, glass nesting tables, and 16-foot ceilings form a seamless palette for showcasing artwork in the sitting room.
Artwork: Ocean Blue Series #26 by Joe Goode. Photo by Kristen McGaughey, <a target="_blank" href="http://www.kmcgphoto.com">kmcgphoto.com</a></figcaption>
            </figure>
            <figure>
                <img src="images/stories/homes-08.jpg" alt="" />
                <figcaption>A telescope placed in the northeast corner of the suspended living room provides a clear view of Blackcomb Mountain. Look slightly west to take in a parade of kayakers or skaters on Alta Lake through architectural glass walls designed especially for that purpose. Photo by Anastasia Chomlack, <a target="_blank" href="http://www.anastasiaphotography.ca">anastasiaphotography.ca</a></figcaption>
            </figure>

            <p>Matt and Nancy chose the property for its proximity to recreation and amenities for their children and for the quiet family neighbourhood, never expecting their children would find such early success in their artistic pursuits and leave home so soon. Shay studies contemporary dance at the Joffrey Ballet School in New York; Cooper is a DJ, concert promoter and music journalist in Los Angeles.</p>
            <p>Still, they have no regrets. Nancy credits Whistler for her children’s great start.</p>
            <p>“Whistler has made them who they are,” she says. Matt agrees.</p>

            <figure>
                <img src="images/stories/homes-09.jpg" alt="" />
                <figcaption>The main floor bedrooms and kitchen open up to a natural courtyard complete with original rock faces, native plants and a tranquil waterfall. Landscaping by SoundGarden Landscape and Design. Photo by Anastasia Chomlack, <a target="_blank" href="http://www.anastasiaphotography.ca">anastasiaphotography.ca</a></figcaption>
            </figure>
