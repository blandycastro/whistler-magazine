            <h2>Turn up the heat this Summer with spicy Whistler creations</h2>
            <h3 class="byline">By Brandon Barrett</h3>

            <p class="story-lead">Whether you're ripping along the bike trails, working on your tan by a local lake or heading out on an alpine hike, it’s not too tough to work up a sweat during Whistler’s summer season.</p>
            <p>And once the sun goes down, the resort’s world-class dining scene can definitely bring the heat, too, especially if you’ve developed a taste for it.</p>
            <p>So grab a cold brew, and take a walk on the spicier side of life as we run down some of Whistler’s favourite fiery foods to titillate your taste buds.</p>

            <h3>Dusty's Bar &amp; BBQ | Ghots Wings</h3>
            <p>Talk to any lover of spicy food long enough, and inevitably the ghost pepper will come up.</p>
            <p>Native to India, where it is commonly used in a “smoke bomb” by farmers trying to keep wild elephants at bay, the ghost pepper has achieved near-mythic status.</p>
            <p>So when diners head to Creekside watering hole Dusty’s Bar &amp; BBQ looking for a plate of their famous Ghost Wings, the servers have to make sure they let these brave diners know exactly what they’re getting themselves into.</p>

            <figure>
                <img src="images/stories/casual-dining-01.jpg" alt="" />
                <figcaption>Ghost Wings at Dusty's Bar &amp; BBQ. Photo by David Buzzard, <a target="_blank" href="http://www.media-centre.ca">media-centre.ca</a></figcaption>
            </figure>

            <p>“It’s the spiciest wing sauce I’ve ever consumed,” says head chef Andrew Eckhart, who blends the restaurant’s housemade hot sauce with ground, dried ghost peppers for a spicy kick you
won’t find anywhere else in Whistler.</p>
            <p>Mercifully, Eckhart explains, the legendary burn of the ghost pepper doesn’t last too long, giving the scorching fruit its spectral moniker.</p>
            <blockquote>“The ghost pepper will burn you intensely and then disappear.”</blockquote>
            <p>“The ghost pepper has a different heat than you’d expect, like from a habanero, which kind of lingers and burns for a while,” he says. “The ghost pepper will burn you intensely and
then disappear.”</p>
            <p>And if the Ghost Wings don’t satisfy your desire for fire, Eckhart said he’s experimenting with smoked habanero peppers to create a wing sauce that’s even hotter.</p>
            <p>Don’t say we didn’t warn you.</p>
            
            <h3>Quattro at Whistler | Spaghetti Quattro</h3>
            <p>When it comes to exquisite cuisine, sometimes simple is better.</p>
            <p>So when restaurateur Antonio Corsi was trying to come up with a namesake dish for his Italian resort eatery, he went with a recipe he knew would keep diners coming back: Spaghetti
Quattro.</p>

            <figure>
                <img src="images/stories/casual-dining-02.jpg" alt="" />
                <figcaption>Spaghetti Quattro. Photo by David Buzzard, <a target="_blank" href="http://www.media-centre.ca">media-centre.ca</a></figcaption>
            </figure>

            <p>“Back when Antonio Corsi opened Quattro, he developed the dish to be spicy but balanced,” says executive chef Jeremie Trottier.</p>
            <p>Spaghetti Quattro appears straightforward enough — the fixings are nothing more than ground chicken, chili flakes, black turtle beans, tomato sauce, garlic and parsley — it’s the
delicate interplay of ingredients that takes the eponymous dish to the next level, Trottier explains.</p>
            <p>“The amount of chilies to garlic to parsley is all very important so it doesn’t just taste like a garlic pasta, or doesn’t just taste like a spicy chili pasta,” he says.</p>
            <p>“It’s the fact that it’s simple, but you can mess it up.”</p>
            
            <h3>The Royal Taste of India | Chili Chicken</h3>
            <p>Many of the world’s revered cuisines have been influenced by the culinary traditions of divergent cultures. The Royal Taste of India’s Chili Chicken is no exception, exemplifying the massive impact globalization has had on India’s contemporary food scene.</p>
            <figure>
                <img src="images/stories/casual-dining-03.jpg" alt="" />
                <figcaption>Chili Chicken. Photo by David Buzzard, <a target="_blank" href="http://www.media-centre.ca">media-centre.ca</a></figcaption>
            </figure>
            <p>“In the last 10 or 15 years, Indians have started to adopt different dishes from different regions,” says owner Jinder Nijjar, who draws from an eclectic well of gastronomic knowledge
himself, having worked in modern French and Italian kitchens before opening his restaurant in Whistler’s Marketplace.</p>
            <p>Fired in a traditional clay Tandoori oven, the juicy chicken breast, red and green peppers and grilled onion feature a hint of smokiness, balanced by the tangy notes of Thai chili sauce and soy sauce that give the dish a distinctly Asian profile.</p>
            <p>A touch of cream will remind you of some of India’s most popular curries without weighing you down, while the use of common Indian spices — including coriander, cardamom and cinnamon — lends the dish a subtle, aromatic quality.</p>
            <blockquote>“In our restaurant we don’t use chilies as much; we try to give our menu more flavour."</blockquote>
            <p>Despite his country’s reputation for scorching hot curries, Nijjar says his philosophy is to delight diners’ senses, not overpower them.</p>
            <p>“In our restaurant we don’t use chilies as much; we try to give our menu more flavour."</p>
            
            <h3>Mexican Corner | Enchiladas Diablo</h3>
            <p>You won’t find Enchiladas Diablo listed anywhere on Mexican Corner’s regular menu. But savvy spice hounds hoping for an extra kick to the traditional Monterrey dish aren’t afraid to make a special request to executive chef Edgar Navarro.</p>
            <figure>
                <img src="images/stories/casual-dining-04.jpg" alt="" />
                <figcaption>Enchiladas Diablo. Photo by David Buzzard, <a target="_blank" href="http://www.media-centre.ca">media-centre.ca</a></figcaption>
            </figure>
            <p>“We try to do this dish because we get a lot of Canadians who want something very spicy,” Navarro explains. “So we add the most peppers we can. It’s going to be a bomb, you know?”
            <p>The Enchiladas Diablo features juicy chorizo sausage stuffed into a soft corn tortilla, topped with fresh shrimp, diced tomatoes and raw jalapeno. Then, it’s drizzled with traditional chilorio sauce made with vinegar and ancho and guajillo peppers, achieving the right note of heat without overpowering your palate.</p>
            <p>“We are trying to give people something spicy, but with flavour,” Navarro says.</p>
            
